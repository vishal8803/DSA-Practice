class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }

    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends

class Solution {
  public:
    int stockBuyAndSell(int n, vector<int> &price) {
        int i = 0;
        int count = 0;
        while(i < n) {
            int j = i+1;
            while(j < n and price[j] >= price[j-1]) {
                count += price[j] - price[j-1];
                j++;
            }
            i = j;
        }
        
        return count;
    }
};


