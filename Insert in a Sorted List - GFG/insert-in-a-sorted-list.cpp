class Solution{
  public:
    
    Node *sortedInsert(struct Node* head, int data) {
        Node* node = new Node(data);
        Node* dummy = new Node(-1);
        dummy->next = head;
        Node* temp = dummy;
        
        while(temp->next and temp->next->data < node->data) {
            temp = temp->next;
        }
        
        if(temp->next) {
            Node* s = temp->next;
            temp->next = node;
            node->next = s;
        } else {
            temp->next = node;
        }
        
        return dummy->next;
    }
};


